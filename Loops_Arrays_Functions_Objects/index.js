// For Loop:
//Printing prime Numbers-----
for (let i = 1; i < 50; i += 2) {
  console.log(i);
}

// While Loop:
let i = 0;
while (i < 5) {
  console.log("HEllo, Himanshu!");
  i++;
}

// print digits
let num = 1234567;
while (num !== 0) {
  let digit = num % 10;
  num = parseInt(num / 10);
  console.log(digit);
}

// Do While Loop:

let d = 0;
do {
  console.log("Hello, Himanshu!");
  d++;
} while (d <= 3);
